    public TelaListaProduto(Lista lista) {
        
        this.selectedLista = lista;
        
        lstListaProdutos = new LinkedList<>();
        lstListaProdutos = ObservableCollections.observableList(lstListaProdutos);
        
        initComponents();
        
        //Passando o nome da lista selecionada para o campo
        txtNameLista.setText(this.selectedLista.getNome());
        
        //Pegando os tipos de produtos cadastrado e listando no combobox
        TipoProdutoDAO tpd = new TipoProdutoDAO();
        
        lstTipoProduto = tpd.listar();
        
        for(TipoProduto tp:lstTipoProduto){
            cbTipoProduto.addItem(tp.getDescricao());
        }
        
        BindingGroup bg = new BindingGroup();
        JTableBinding tb = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ_WRITE,
            lstListaProdutos, tabelaListaProdutos);
        
        tabelaListaProdutos.setRowHeight(50);
       
        JTableBinding.ColumnBinding cbImg = tb.addColumnBinding(BeanProperty.create("produto.imagem"));
        cbImg.setColumnName("Imagem");
        cbImg.setColumnClass(ImageIcon.class);
        
        JTableBinding.ColumnBinding cbDesc = tb.addColumnBinding(BeanProperty.create("tipoProduto.descricao"));
        cbDesc.setColumnName("Descrição");
        cbDesc.setColumnClass(String.class);
        
        JTableBinding.ColumnBinding cbProd = tb.addColumnBinding(BeanProperty.create("produto.nome"));
        cbProd.setColumnName("Produto");
        cbProd.setColumnClass(String.class);
        
        JTableBinding.ColumnBinding cbPrice = tb.addColumnBinding(BeanProperty.create("produto.preco"));
        cbPrice.setColumnName("Preço");
        cbPrice.setColumnClass(String.class);
        
        
        bg.addBinding(tb);
        
        bg.bind();
        
        tabelaListaProdutos.getColumnModel().getColumn(0).setCellRenderer(new RenderImage());
        
    }

