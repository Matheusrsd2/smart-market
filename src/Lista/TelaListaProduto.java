    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TipoProduto tipoProduto = new TipoProduto();

        for(TipoProduto tp:lstTipoProduto){
            cbTipoProduto.addItem(tp.getDescricao());

            if(cbTipoProduto.getSelectedItem() == tp.getDescricao()){
                tipoProduto = tp;
            }
        }

        Produto produto = new Produto();

        ListaProduto listaProduto = new ListaProduto();
        listaProduto.setLista(this.selectedLista);
        listaProduto.setTipoProduto(tipoProduto);
        listaProduto.setProduto(produto);

        lstListaProdutos.add(listaProduto);

        ListaProdutoDAO lpDAO = new ListaProdutoDAO();
    }//GEN-LAST:event_jButton1ActionPerformed

