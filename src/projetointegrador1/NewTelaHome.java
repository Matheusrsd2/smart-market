    public void isLogged(Cliente user, boolean status){
        if(status){
            btnLogin.setVisible(false);
            btnCadastro.setVisible(false);
            btnPerfil.setVisible(true);
            
            conteudo.removeAll();
            
            BuscaProduto bp = new BuscaProduto();
            bp.setBounds(0, 0, conteudo.getWidth(), conteudo.getHeight());
            bp.setVisible(true);
            conteudo.add(bp);
            this.repaint();
            this.revalidate();
            
            this.clienteLogado = user;
            System.out.printf("User ADM: %b", user.getAdministrador());
        }
        else{
            btnLogin.setVisible(true);
            btnCadastro.setVisible(true);
        }
    }
    
